window.onbeforeunload = function (e) {
    // Customize the alert message
    let confirmationMessage = "Are you sure you want to refresh this page? If you refresh this page, your test will be over, and the result will not be submitted. If you still want to refresh, click 'Yes'. Otherwise, click 'No'.";
    
    // Create a confirmation dialog
    if (confirm(confirmationMessage)) {
        // If the user clicks 'Yes', refresh the page (which is the default behavior, so we return true)
        return true;
    } else {
        // If the user clicks 'No', cancel the refresh
        e.preventDefault(); // Prevents the default refresh behavior
        e.returnValue = ''; // For some browsers compatibility (optional)
        // Darken the 'No' button or add any other action to indicate the decision
        darkenNoButton(); // This will call the function to darken the 'No' button
        return false;
    }
};

// Function to darken the 'No' button
function darkenNoButton() {
    let noButton = document.createElement("button");
    noButton.innerText = "No";
    noButton.style.backgroundColor = "darkgray"; // Darken the button to avoid mistakes
    noButton.disabled = true; // Disable the button to prevent future clicks
    document.body.appendChild(noButton); // Append the button to the body or any other container
}

