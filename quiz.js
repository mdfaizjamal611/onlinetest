        function startCountdown(duration, display) {
            var timer = duration, minutes, seconds;
            setInterval(function() {
                minutes = parseInt(timer / 60, 10);
                seconds = parseInt(timer % 60, 10);

                minutes = minutes < 10 ? "0" + minutes : minutes;
                seconds = seconds < 10 ? "0" + seconds : seconds;

                display.textContent = minutes + ":" + seconds;

                if (--timer < 0) {
                    window.location.href = "index.html"; // Redirect to login page when time is up
                }
            }, 1000);
        }

        window.onload = function() {
            var countdownTime = 120 * 60; // 120 minutes (2 hours) in seconds
            var display = document.querySelector('#countdown');
            startCountdown(countdownTime, display);
        };

function nextQuestion() {
    const selectedOption = document.querySelector('input[name="option"]:checked');
    if (selectedOption) {
        userAnswers[currentQuestionIndex] = parseInt(selectedOption.value);
    }

    if (currentQuestionIndex < questions.length - 1) {
        currentQuestionIndex++;
        loadQuestion();
    }
}

function prevQuestion() {
    const selectedOption = document.querySelector('input[name="option"]:checked');
    if (selectedOption) {
        userAnswers[currentQuestionIndex] = parseInt(selectedOption.value);
    }

    if (currentQuestionIndex > 0) {
        currentQuestionIndex--;
        loadQuestion();
    }
}

function submitQuiz() {
    const selectedOption = document.querySelector('input[name="option"]:checked');
    if (selectedOption) {
        userAnswers[currentQuestionIndex] = parseInt(selectedOption.value);
    }

    let score = 0;
    for (let i = 0; i < questions.length; i++) {
        if (userAnswers[i] === questions[i].answer) {
            score++;
        }
    }

    const userId = localStorage.getItem('userId');
    if (userId) {
        const results = JSON.parse(localStorage.getItem('results')) || {};
        results[userId] = score;
        localStorage.setItem('results', JSON.stringify(results));
    }

    alert('Quiz submitted! Your score: ' + score);
    window.location.href = 'index.html';
}

