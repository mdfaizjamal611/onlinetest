let currentQuestionIndex = 0;
let userAnswers = Array(questions.length).fill(null);

function loadQuestion() {
    const questionNumber = document.getElementById('questionNumber'); // Reference to the question number element
    const questionImage = document.getElementById('questionImage'); // Reference to the image element
    const option1 = document.getElementById('label1');
    const option2 = document.getElementById('label2');
    const option3 = document.getElementById('label3');
    const option4 = document.getElementById('label4');

    const currentQuestion = questions[currentQuestionIndex];
    // Set the question number text
    questionNumber.textContent = `Q${currentQuestionIndex + 1}`;
    questionImage.src = `upload/${currentQuestion.image}`; // Set the source of the question image
    option1.textContent = currentQuestion.options[0];
    option2.textContent = currentQuestion.options[1];
    option3.textContent = currentQuestion.options[2];
    option4.textContent = currentQuestion.options[3];

    const selectedOption = userAnswers[currentQuestionIndex];
    document.querySelectorAll('input[name="option"]').forEach((input, index) => {
        input.checked = index === selectedOption;
    });

    updateNavigationButtons();
}


function updateNavigationButtons() {
    document.getElementById('prevBtn').style.display = currentQuestionIndex === 0 ? 'none' : 'inline-block';
    document.getElementById('nextBtn').style.display = currentQuestionIndex === questions.length - 1 ? 'none' : 'inline-block';
    document.getElementById('submitBtn').style.display = currentQuestionIndex === questions.length - 1 ? 'inline-block' : 'none';
}


